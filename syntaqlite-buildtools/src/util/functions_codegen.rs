//! Generates a Rust source file containing the SQLite built-in function catalog.
//!
//! Reads `functions.json` (extracted from SQLite source) and emits a static
//! array of `FunctionEntry` values with availability rules that can be filtered
//! at runtime by `DialectConfig`.

use std::fmt::Write;

use serde::Deserialize;

use super::AUTOGENERATED_MARKER;

// ── JSON schema ─────────────────────────────────────────────────────

#[derive(Deserialize)]
struct FunctionsFile {
    functions: Vec<JsonFunction>,
}

#[derive(Deserialize)]
struct JsonFunction {
    name: String,
    arities: Vec<i16>,
    category: String,
    availability: Vec<JsonAvailability>,
}

#[derive(Deserialize)]
struct JsonAvailability {
    since: String,
    until: Option<String>,
    cflag: Option<String>,
    polarity: Option<String>,
}

// ── Version encoding ────────────────────────────────────────────────

fn encode_version(s: &str) -> i32 {
    let parts: Vec<&str> = s.split('.').collect();
    if parts.len() != 3 {
        panic!("bad version string: {s}");
    }
    let major: i32 = parts[0].parse().unwrap();
    let minor: i32 = parts[1].parse().unwrap();
    let patch: i32 = parts[2].parse().unwrap();
    major * 1_000_000 + minor * 1_000 + patch
}

// ── Cflag name → index mapping ──────────────────────────────────────

/// Map a cflag name (e.g. `"SQLITE_OMIT_JSON"`) to its `SYNQ_CFLAG_IDX_*` index.
///
/// The mapping is derived from the C header constants. We hardcode the known
/// set here rather than parsing the header at codegen time, since the set is
/// stable and tracked by the cflags header.
fn cflag_index(name: &str) -> Option<u32> {
    let suffix = name.strip_prefix("SQLITE_")?;
    match suffix {
        "OMIT_ALTERTABLE" => Some(0),
        "OMIT_ANALYZE" => Some(1),
        "OMIT_ATTACH" => Some(2),
        "OMIT_AUTOINCREMENT" => Some(3),
        "OMIT_CAST" => Some(4),
        "OMIT_COMPILEOPTION_DIAGS" => Some(5),
        "OMIT_COMPOUND_SELECT" => Some(6),
        "OMIT_CTE" => Some(7),
        "OMIT_DATETIME_FUNCS" => Some(8),
        "OMIT_EXPLAIN" => Some(9),
        "OMIT_FLOATING_POINT" => Some(10),
        "OMIT_FOREIGN_KEY" => Some(11),
        "OMIT_GENERATED_COLUMNS" => Some(12),
        "OMIT_JSON" => Some(13),
        "OMIT_LOAD_EXTENSION" => Some(14),
        "OMIT_PRAGMA" => Some(15),
        "OMIT_REINDEX" => Some(16),
        "OMIT_RETURNING" => Some(17),
        "OMIT_SUBQUERY" => Some(18),
        "OMIT_TEMPDB" => Some(19),
        "OMIT_TRIGGER" => Some(20),
        "OMIT_VACUUM" => Some(21),
        "OMIT_VIEW" => Some(22),
        "OMIT_VIRTUALTABLE" => Some(23),
        "OMIT_WINDOWFUNC" => Some(24),
        "ENABLE_BYTECODE_VTAB" => Some(25),
        "ENABLE_CARRAY" => Some(26),
        "ENABLE_DBPAGE_VTAB" => Some(27),
        "ENABLE_DBSTAT_VTAB" => Some(28),
        "ENABLE_FTS3" => Some(29),
        "ENABLE_FTS4" => Some(30),
        "ENABLE_FTS5" => Some(31),
        "ENABLE_GEOPOLY" => Some(32),
        "ENABLE_JSON1" => Some(33),
        "ENABLE_MATH_FUNCTIONS" => Some(34),
        "ENABLE_OFFSET_SQL_FUNC" => Some(35),
        "ENABLE_ORDERED_SET_AGGREGATES" => Some(36),
        "ENABLE_PERCENTILE" => Some(37),
        "ENABLE_RTREE" => Some(38),
        "ENABLE_STMTVTAB" => Some(39),
        "ENABLE_UPDATE_DELETE_LIMIT" => Some(40),
        "SOUNDEX" => Some(41),
        _ => None,
    }
}

// ── Code generation ─────────────────────────────────────────────────

/// Generate the `functions_catalog.rs` Rust source from `functions.json` content.
pub fn generate_functions_catalog(json_content: &str) -> Result<String, String> {
    let file: FunctionsFile =
        serde_json::from_str(json_content).map_err(|e| format!("parsing functions.json: {e}"))?;

    let mut out = String::with_capacity(32 * 1024);

    // Header
    writeln!(out, "// {AUTOGENERATED_MARKER}").unwrap();
    writeln!(out).unwrap();
    writeln!(
        out,
        "//! Static catalog of SQLite built-in functions with version/cflag availability."
    )
    .unwrap();
    writeln!(out).unwrap();
    writeln!(out, "use crate::dialect::ffi::DialectConfig;").unwrap();
    writeln!(out).unwrap();

    // Types
    writeln!(out, "/// Category of a built-in function.").unwrap();
    writeln!(out, "#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash)]").unwrap();
    writeln!(out, "pub enum FunctionCategory {{").unwrap();
    writeln!(out, "    Scalar,").unwrap();
    writeln!(out, "    Aggregate,").unwrap();
    writeln!(out, "    Window,").unwrap();
    writeln!(out, "}}").unwrap();
    writeln!(out).unwrap();

    writeln!(out, "/// Whether a cflag enables or omits the function.").unwrap();
    writeln!(out, "#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash)]").unwrap();
    writeln!(out, "pub enum CflagPolarity {{").unwrap();
    writeln!(
        out,
        "    /// Function requires the cflag to be set (SQLITE_ENABLE_*)."
    )
    .unwrap();
    writeln!(out, "    Enable,").unwrap();
    writeln!(
        out,
        "    /// Function is omitted when the cflag is set (SQLITE_OMIT_*)."
    )
    .unwrap();
    writeln!(out, "    Omit,").unwrap();
    writeln!(out, "}}").unwrap();
    writeln!(out).unwrap();

    writeln!(out, "/// Metadata about a built-in function.").unwrap();
    writeln!(out, "#[derive(Debug, Clone, Copy)]").unwrap();
    writeln!(out, "pub struct FunctionInfo {{").unwrap();
    writeln!(out, "    /// Function name (lowercase).").unwrap();
    writeln!(out, "    pub name: &'static str,").unwrap();
    writeln!(
        out,
        "    /// Supported arities. Negative values indicate variadic:"
    )
    .unwrap();
    writeln!(
        out,
        "    /// -1 = any number of args, -N = at least N-1 args."
    )
    .unwrap();
    writeln!(out, "    pub arities: &'static [i16],").unwrap();
    writeln!(out, "    /// Function category.").unwrap();
    writeln!(out, "    pub category: FunctionCategory,").unwrap();
    writeln!(out, "}}").unwrap();
    writeln!(out).unwrap();

    writeln!(out, "/// A version/cflag availability rule for a function.").unwrap();
    writeln!(out, "#[derive(Debug, Clone, Copy)]").unwrap();
    writeln!(out, "pub struct AvailabilityRule {{").unwrap();
    writeln!(
        out,
        "    /// Minimum SQLite version (encoded: major*1_000_000 + minor*1_000 + patch)."
    )
    .unwrap();
    writeln!(out, "    pub since: i32,").unwrap();
    writeln!(
        out,
        "    /// Maximum SQLite version (exclusive). 0 means no upper bound."
    )
    .unwrap();
    writeln!(out, "    pub until: i32,").unwrap();
    writeln!(
        out,
        "    /// Cflag bit index in `Cflags`, or `u32::MAX` if no cflag required."
    )
    .unwrap();
    writeln!(out, "    pub cflag_index: u32,").unwrap();
    writeln!(out, "    /// Polarity of the cflag constraint.").unwrap();
    writeln!(out, "    pub cflag_polarity: CflagPolarity,").unwrap();
    writeln!(out, "}}").unwrap();
    writeln!(out).unwrap();

    writeln!(
        out,
        "/// A function entry combining metadata with availability rules."
    )
    .unwrap();
    writeln!(out, "#[derive(Debug, Clone, Copy)]").unwrap();
    writeln!(out, "pub struct FunctionEntry {{").unwrap();
    writeln!(out, "    pub info: FunctionInfo,").unwrap();
    writeln!(out, "    pub availability: &'static [AvailabilityRule],").unwrap();
    writeln!(out, "}}").unwrap();
    writeln!(out).unwrap();

    // Static data: arity arrays
    for func in &file.functions {
        let ident = arity_ident(&func.name);
        write!(out, "static ARITIES_{ident}: &[i16] = &[").unwrap();
        for (i, a) in func.arities.iter().enumerate() {
            if i > 0 {
                write!(out, ", ").unwrap();
            }
            write!(out, "{a}").unwrap();
        }
        writeln!(out, "];").unwrap();
    }
    writeln!(out).unwrap();

    // Static data: availability arrays
    for func in &file.functions {
        let ident = arity_ident(&func.name);
        write!(out, "static AVAIL_{ident}: &[AvailabilityRule] = &[").unwrap();
        for (i, avail) in func.availability.iter().enumerate() {
            if i > 0 {
                write!(out, ", ").unwrap();
            }
            let since = encode_version(&avail.since);
            let until = avail.until.as_deref().map(encode_version).unwrap_or(0);
            let (cflag_idx, polarity) = match &avail.cflag {
                Some(name) => {
                    let idx = cflag_index(name).ok_or_else(|| {
                        format!("unknown cflag '{}' in function '{}'", name, func.name)
                    })?;
                    let pol = match avail.polarity.as_deref() {
                        Some("enable") => "CflagPolarity::Enable",
                        Some("omit") => "CflagPolarity::Omit",
                        other => {
                            return Err(format!(
                                "unknown polarity '{:?}' for cflag '{}' in function '{}'",
                                other, name, func.name
                            ));
                        }
                    };
                    (idx, pol)
                }
                None => (u32::MAX, "CflagPolarity::Enable"),
            };
            write!(
                out,
                "AvailabilityRule {{ since: {since}, until: {until}, cflag_index: {cflag_idx}, cflag_polarity: {polarity} }}"
            )
            .unwrap();
        }
        writeln!(out, "];").unwrap();
    }
    writeln!(out).unwrap();

    // Main catalog array
    writeln!(
        out,
        "/// All {count} SQLite built-in functions.",
        count = file.functions.len()
    )
    .unwrap();
    writeln!(out, "pub static SQLITE_FUNCTIONS: &[FunctionEntry] = &[").unwrap();
    for func in &file.functions {
        let ident = arity_ident(&func.name);
        let cat = match func.category.as_str() {
            "scalar" => "FunctionCategory::Scalar",
            "aggregate" => "FunctionCategory::Aggregate",
            "window" => "FunctionCategory::Window",
            other => {
                return Err(format!(
                    "unknown category '{other}' for function '{}'",
                    func.name
                ));
            }
        };
        let name_escaped = func.name.replace('\\', "\\\\").replace('"', "\\\"");
        writeln!(out, "    FunctionEntry {{").unwrap();
        writeln!(
            out,
            "        info: FunctionInfo {{ name: \"{name_escaped}\", arities: ARITIES_{ident}, category: {cat} }},"
        )
        .unwrap();
        writeln!(out, "        availability: AVAIL_{ident},").unwrap();
        writeln!(out, "    }},").unwrap();
    }
    writeln!(out, "];").unwrap();
    writeln!(out).unwrap();

    // Availability check function
    writeln!(
        out,
        "/// Check whether a function entry is available for the given dialect config."
    )
    .unwrap();
    writeln!(out, "///").unwrap();
    writeln!(
        out,
        "/// A function is available if *any* of its availability rules matches."
    )
    .unwrap();
    writeln!(out, "/// A rule matches when:").unwrap();
    writeln!(out, "/// - The config version is >= `since`").unwrap();
    writeln!(
        out,
        "/// - The config version is < `until` (if `until` is non-zero)"
    )
    .unwrap();
    writeln!(
        out,
        "/// - If a cflag is required: for `Enable` polarity, the cflag must be set;"
    )
    .unwrap();
    writeln!(out, "///   for `Omit` polarity, the cflag must NOT be set.").unwrap();
    writeln!(
        out,
        "pub fn is_available(entry: &FunctionEntry, config: &DialectConfig) -> bool {{"
    )
    .unwrap();
    writeln!(out, "    entry.availability.iter().any(|rule| {{").unwrap();
    writeln!(out, "        if config.sqlite_version < rule.since {{").unwrap();
    writeln!(out, "            return false;").unwrap();
    writeln!(out, "        }}").unwrap();
    writeln!(
        out,
        "        if rule.until != 0 && config.sqlite_version >= rule.until {{"
    )
    .unwrap();
    writeln!(out, "            return false;").unwrap();
    writeln!(out, "        }}").unwrap();
    writeln!(out, "        if rule.cflag_index != u32::MAX {{").unwrap();
    writeln!(
        out,
        "            let flag_set = config.cflags.has(rule.cflag_index);"
    )
    .unwrap();
    writeln!(out, "            match rule.cflag_polarity {{").unwrap();
    writeln!(out, "                CflagPolarity::Enable => flag_set,").unwrap();
    writeln!(out, "                CflagPolarity::Omit => !flag_set,").unwrap();
    writeln!(out, "            }}").unwrap();
    writeln!(out, "        }} else {{").unwrap();
    writeln!(out, "            true").unwrap();
    writeln!(out, "        }}").unwrap();
    writeln!(out, "    }})").unwrap();
    writeln!(out, "}}").unwrap();

    Ok(out)
}

/// Convert a function name to a valid Rust identifier for use in static names.
fn arity_ident(name: &str) -> String {
    let mut out = String::with_capacity(name.len());
    for ch in name.chars() {
        match ch {
            'a'..='z' | 'A'..='Z' | '0'..='9' | '_' => out.push(ch.to_ascii_uppercase()),
            '-' | '>' => out.push('_'),
            _ => {
                // Fallback: hex encode
                write!(out, "X{:02X}", ch as u32).unwrap();
            }
        }
    }
    out
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn arity_ident_basic() {
        assert_eq!(arity_ident("abs"), "ABS");
        assert_eq!(arity_ident("->"), "__");
        assert_eq!(arity_ident("->>"), "___");
        assert_eq!(arity_ident("json_array"), "JSON_ARRAY");
    }

    #[test]
    fn generate_from_minimal_json() {
        let json = r#"{
            "functions": [
                {
                    "name": "abs",
                    "arities": [0, 1],
                    "category": "scalar",
                    "availability": [
                        { "since": "3.30.1" }
                    ]
                }
            ]
        }"#;
        let result = generate_functions_catalog(json).unwrap();
        assert!(result.contains("SQLITE_FUNCTIONS"));
        assert!(result.contains("\"abs\""));
        assert!(result.contains("FunctionCategory::Scalar"));
        assert!(result.contains("is_available"));
    }

    #[test]
    fn generate_with_cflag() {
        let json = r#"{
            "functions": [
                {
                    "name": "acos",
                    "arities": [1],
                    "category": "scalar",
                    "availability": [
                        {
                            "since": "3.35.5",
                            "cflag": "SQLITE_ENABLE_MATH_FUNCTIONS",
                            "polarity": "enable"
                        }
                    ]
                }
            ]
        }"#;
        let result = generate_functions_catalog(json).unwrap();
        assert!(result.contains("cflag_index: 34"));
        assert!(result.contains("CflagPolarity::Enable"));
    }
}
