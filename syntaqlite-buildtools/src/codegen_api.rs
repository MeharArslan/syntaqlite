fn parse_synq_items(
    synq_files: &[(String, String)],
) -> Result<Vec<util::synq_parser::Item>, String> {
    let mut all_items = Vec::new();
    for (name, content) in synq_files {
        let items =
            util::synq_parser::parse_synq_file(content).map_err(|e| format!("{name}: {e}"))?;
        all_items.extend(items);
    }
    Ok(all_items)
}

pub fn generate_codegen_artifacts(
    request: &CodegenRequest<'_>,
) -> Result<CodegenArtifacts, String> {
    let parser_name = request.parser_symbol_prefix.unwrap_or("SynqSqliteParse");

    let all_items = parse_synq_items(request.synq_files)?;

    // For extension builds, parse base and extension items separately.
    // Both vecs must outlive the AstModel, so declare them here.
    let base_items_owned: Vec<util::synq_parser::Item>;
    let ext_items_owned: Vec<util::synq_parser::Item>;

    let ast_model = if let Some(base_synq) = request.base_synq_files {
        let base_owned: Vec<(String, String)> = base_synq
            .iter()
            .map(|(n, c)| (n.to_string(), c.to_string()))
            .collect();
        base_items_owned = parse_synq_items(&base_owned)?;

        let base_names: std::collections::HashSet<&str> =
            base_synq.iter().map(|(n, _)| *n).collect();
        let ext_files: Vec<(String, String)> = request
            .synq_files
            .iter()
            .filter(|(n, _)| !base_names.contains(n.as_str()))
            .map(|(n, c)| (n.clone(), c.clone()))
            .collect();
        ext_items_owned = if ext_files.is_empty() {
            Vec::new()
        } else {
            parse_synq_items(&ext_files)?
        };

        dialect_codegen::AstModel::new_with_extensions(&base_items_owned, &ext_items_owned)
            .map_err(|e| format!("extension validation: {e}"))?
    } else {
        dialect_codegen::AstModel::new(&all_items)
    };

    let work_dir =
        tempfile::TempDir::new().map_err(|e| format!("Failed to create temp directory: {e}"))?;
    parser_pipeline::generate_parser_from_contents(
        request.y_files,
        parser_name,
        work_dir.path().to_string_lossy().as_ref(),
        Some(request.dialect_c_includes.tokens_header),
    )?;
    let parse_h = fs::read_to_string(work_dir.path().join("parse.h"))
        .map_err(|e| format!("Failed to read parse.h: {e}"))?;
    let parse_c = fs::read_to_string(work_dir.path().join("parse.c"))
        .map_err(|e| format!("Failed to read parse.c: {e}"))?;

    // Assemble tokenizer and keyword hash from pre-extracted fragments.
    let fragments = sqlite_fragments::load();
    let (tokenize_c, extract_result) = tokenizer_assembly::assemble(
        &fragments,
        request.dialect.name(),
        &request.dialect_c_includes,
    )?;
    let keyword_c = keyword_hash::generate(
        &extract_result,
        &fragments,
        request.dialect.name(),
        request.extra_keywords,
        &request.dialect_c_includes,
    )?;
    let keyword_h = keyword_hash::generate_keyword_h();

    let ast_nodes_h = ast_model.generate_ast_nodes_header(request.dialect.name());
    let ast_builder_h = ast_model.generate_ast_builder_header(request.dialect.name());
    let dialect_meta_h = {
        let mut meta = ast_model
            .generate_c_field_metadata(request.dialect.name())
            .map_err(|e: CMetaCodegenError| e.to_string())?;
        let schema = ast_model.generate_c_schema_contributions();
        if !schema.is_empty() {
            // Insert schema contributions before the header guard end.
            let guard_end = "#endif  // SYNTAQLITE_DIALECT_META_H";
            if let Some(pos) = meta.find(guard_end) {
                meta.insert_str(pos, &schema);
            } else {
                meta.push_str(&schema);
            }
        }
        meta
    };
    let dialect_fmt_h = ast_model
        .generate_c_fmt_tables()
        .map_err(|e: CFmtCodegenError| e.to_string())?;
    let token_defines = extract_token_defines(&parse_h);
    // Build keyword set from the base mkkeywordhash table + dialect extra keywords.
    let mut keyword_names = base_keyword_token_names();
    for kw in request.extra_keywords {
        keyword_names.insert(kw.to_uppercase());
    }
    let dialect_tokens_h = generate_token_categories_header(&token_defines, Some(&keyword_names));
    let parse_api_h = generate_parse_h(request.dialect.name());
    let tokenize_h = generate_tokenize_h(request.dialect.name());
    let dialect_c = generate_dialect_c(
        request.dialect.name(),
        Some(&token_defines),
        &request.dialect_c_includes,
    );
    let dialect_h = generate_dialect_h(request.dialect.name());
    let dialect_dispatch_h = generate_dialect_dispatch_h(request.dialect.name());

    let rust = if request.include_rust {
        // All generated Rust files import from `syntaqlite_parser` directly.
        // Both the internal SQLite dialect crate (syntaqlite-parser-sqlite) and
        // external dialect crates declare `syntaqlite-parser` as a dependency.
        let rust_paths = RustAstPaths {
            crate_prefix: "syntaqlite_parser",
            // ffi_path: dialect-specific FFI structs always live in crate::ffi.
            ffi_path: "crate::ffi",
            comment_path: "syntaqlite_parser",
            nodes_path: "syntaqlite_parser",
            session_path: "syntaqlite_parser",
            dialect_fn_path: "crate::dialect::dialect",
        };
        Some(RustCodegenArtifacts {
            tokens_rs: generate_rust_tokens(&token_defines[..]),
            ffi_rs: ast_model.generate_rust_ffi_nodes(&rust_paths),
            ast_rs: ast_model.generate_rust_ast(
                &rust_paths,
                request.dialect.name(),
                request.open_for_extension,
            ),
            ast_traits_rs: Some(ast_model.generate_ast_traits()),
            lib_rs: generate_rust_lib(&request.dialect.dialect_symbol_fn_name()),
            build_rs: generate_rust_build_rs(request.dialect.name()),
            cargo_toml: generate_cargo_toml(request.crate_name.unwrap_or(request.dialect.name())),
            functions_catalog_rs: None,
        })
    } else {
        None
    };

    let runtime_tokens_h = generate_runtime_tokens_header(&token_defines);

    Ok(CodegenArtifacts {
        parse_h,
        parse_api_h,
        parse_c,
        tokenize_c,
        tokenize_h,
        keyword_c,
        keyword_h,
        ast_nodes_h,
        ast_builder_h,
        dialect_meta_h,
        dialect_fmt_h,
        dialect_tokens_h,
        dialect_c,
        dialect_h,
        dialect_dispatch_h,
        runtime_tokens_h,
        rust,
    })
}

pub fn read_named_files_from_dir(dir: &str, ext: &str) -> Result<Vec<(String, String)>, String> {
    let dir_path = std::path::Path::new(dir);
    if !dir_path.is_dir() {
        return Err(format!("{dir} is not a directory"));
    }

    let mut files: Vec<(String, String)> = fs::read_dir(dir_path)
        .map_err(|e| format!("Failed to read directory {dir}: {e}"))?
        .filter_map(|entry| {
            let path = entry.ok()?.path();
            if path.extension()?.to_str()? == ext {
                let name = path.file_name()?.to_str()?.to_string();
                let content = fs::read_to_string(&path).ok()?;
                Some((name, content))
            } else {
                None
            }
        })
        .collect();
    files.sort_by(|a, b| a.0.cmp(&b.0));
    Ok(files)
}
