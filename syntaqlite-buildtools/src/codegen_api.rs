pub struct CodegenRequest<'a> {
    pub dialect: &'a DialectNaming,
    pub y_files: &'a [(String, String)],
    pub synq_files: &'a [(String, String)],
    pub extra_keywords: &'a [String],
    pub parser_symbol_prefix: Option<&'a str>,
    pub include_rust: bool,
    /// Crate name for `Cargo.toml` (e.g. `"syntaqlite"`, `"syntaqlite-libsql"`).
    /// Only used when `include_rust` is true.
    pub crate_name: Option<&'a str>,
    /// Base .synq files for extension builds. When present, base items get
    /// pinned tags and extension items get tags after the base range.
    /// When `None`, all items from `synq_files` are treated as base.
    pub base_synq_files: Option<&'a [(&'a str, &'a str)]>,
    /// When `true`, the generated `Node<'a>` enum gets an `Other { id, tag }`
    /// variant for unknown tags (dialect extension support). When `false`,
    /// unknown tags are unreachable.
    pub open_for_extension: bool,
    /// Include path prefixes for `dialect.c`. Defaults to `""` for both
    /// internal and public — suitable for external dialect crates where
    /// all headers live in a single output directory.
    pub dialect_c_includes: DialectCIncludes<'a>,
}

/// A prepared codegen job for an external or amalgamated dialect.
///
/// Captures the merged file inputs and pre-computes `extra_keywords` (terminal
/// symbols in extension `.y` files that aren't in the base grammar).
///
/// Call [`write_to`](DialectCodegenJob::write_to) with an
/// [`OutputLayout`](crate::output_resolver::OutputLayout) to run the full
/// pipeline and write all artifacts.
pub struct DialectCodegenJob<'a> {
    dialect: &'a DialectNaming,
    y_files: &'a [(String, String)],
    synq_files: &'a [(String, String)],
    extra_keywords: Vec<String>,
    base_synq_files: Option<&'a [(&'a str, &'a str)]>,
}

impl<'a> DialectCodegenJob<'a> {
    /// Create a new job from merged (base + extension) file sets.
    ///
    /// `extra_keywords` are extracted automatically from extension `.y` files —
    /// those not present in [`base_y_files`](crate::base_files::base_y_files).
    pub fn new(
        dialect: &'a DialectNaming,
        y_files: &'a [(String, String)],
        synq_files: &'a [(String, String)],
    ) -> Self {
        let ext_y_contents: Vec<&str> = y_files
            .iter()
            .filter(|(name, _)| {
                !crate::base_files::base_y_files()
                    .iter()
                    .any(|(base_name, _)| *base_name == name.as_str())
            })
            .map(|(_, content)| content.as_str())
            .collect();
        let extra_keywords = extract_terminals_from_y(&ext_y_contents);
        Self {
            dialect,
            y_files,
            synq_files,
            extra_keywords,
            base_synq_files: None,
        }
    }

    /// Pin the base `.synq` files as a baseline for extension tag numbering.
    pub fn with_base_synq(mut self, files: &'a [(&'a str, &'a str)]) -> Self {
        self.base_synq_files = Some(files);
        self
    }

    /// Run the full codegen pipeline and write all artifacts to `layout`.
    pub fn write_to(
        &self,
        layout: &crate::output_resolver::OutputLayout,
        ensure_dir: &impl Fn(&std::path::Path) -> Result<(), String>,
        write_file: &impl Fn(&std::path::Path, &str) -> Result<(), String>,
    ) -> Result<(), String> {
        let parser_prefix = self.dialect.parser_symbol_prefix();
        let request = CodegenRequest {
            dialect: self.dialect,
            y_files: self.y_files,
            synq_files: self.synq_files,
            extra_keywords: &self.extra_keywords,
            parser_symbol_prefix: Some(&parser_prefix),
            include_rust: false,
            crate_name: None,
            base_synq_files: self.base_synq_files,
            open_for_extension: false,
            dialect_c_includes: layout.c_includes(),
        };
        let artifacts = generate_codegen_artifacts(&request)?;
        layout.write_codegen_artifacts(self.dialect, artifacts, ensure_dir, write_file)
    }
}

/// Return the set of token names that are keywords in the base SQLite table.
pub fn base_keyword_token_names() -> std::collections::HashSet<String> {
    mkkeyword::base_keyword_token_names()
}

/// Extract terminal symbols (potential keywords) from extension `.y` grammar files.
pub fn extract_terminals_from_y(extension_y_contents: &[&str]) -> Vec<String> {
    use std::collections::HashSet;

    let mut terminals: HashSet<String> = HashSet::new();

    for content in extension_y_contents {
        let grammar = match util::grammar_parser::LemonGrammar::parse(content) {
            Ok(g) => g,
            Err(_) => continue,
        };

        for tok in &grammar.tokens {
            if is_keyword_like(tok.name) {
                terminals.insert(tok.name.to_string());
            }
        }

        for fb in &grammar.fallbacks {
            for tok in &fb.tokens {
                if is_keyword_like(tok) {
                    terminals.insert(tok.to_string());
                }
            }
        }

        for rule in &grammar.rules {
            for sym in &rule.rhs {
                if is_keyword_like(sym.name) && sym.name != "ID" {
                    terminals.insert(sym.name.to_string());
                }
            }
        }
    }

    terminals.into_iter().collect()
}

fn is_keyword_like(name: &str) -> bool {
    name.len() >= 2 && name.chars().all(|c| c.is_ascii_uppercase())
}

#[cfg(test)]
mod tests {
    use std::collections::BTreeSet;

    #[test]
    fn extract_terminals_collects_rhs_tokens_but_excludes_id() {
        let y = r#"
%token PERFETTO MACRO.
%fallback ID PERFETTO MODULE.
cmd ::= INCLUDE PERFETTO MODULE ID DOT ID.
cmd ::= CREATE PERFETTO MACRO ID LP RP AS ANY.
"#;
        let got: BTreeSet<String> = super::extract_terminals_from_y(&[y]).into_iter().collect();
        let want: BTreeSet<String> = [
            "ANY", "AS", "CREATE", "DOT", "INCLUDE", "LP", "MACRO", "MODULE", "PERFETTO", "RP",
        ]
        .into_iter()
        .map(str::to_string)
        .collect();
        assert_eq!(got, want);
        assert!(!got.contains("ID"));
    }
}

