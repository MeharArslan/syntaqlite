#[derive(Debug, Clone)]
pub struct DialectNaming {
    name: String,
}

impl DialectNaming {
    pub fn new(name: impl Into<String>) -> Self {
        Self { name: name.into() }
    }

    pub fn name(&self) -> &str {
        &self.name
    }

    pub fn include_dir_name(&self) -> String {
        format!("syntaqlite_{}", self.name)
    }

    pub fn node_header_name(&self) -> String {
        format!("{}_node.h", self.name)
    }

    pub fn dialect_header_name(&self) -> String {
        format!("{}.h", self.name)
    }

    pub fn dialect_dispatch_header_name(&self) -> String {
        format!("{}_dialect_dispatch.h", self.name)
    }

    pub fn dialect_symbol_fn_name(&self) -> String {
        format!("syntaqlite_{}_dialect", self.name)
    }

    pub fn parser_symbol_prefix(&self) -> String {
        format!("Synq{}Parse", util::pascal_case(&self.name))
    }

    /// File name for the dialect-specific token constants header.
    /// E.g. `sqlite_tokens.h` for the `sqlite` dialect.
    pub fn tokens_header_name(&self) -> String {
        format!("{}_tokens.h", self.name)
    }

    /// Wrap `parse_h` content in a dialect-specific include guard.
    pub fn guarded_tokens_header(&self, parse_h: &str) -> String {
        let guard = format!(
            "SYNTAQLITE_{}_TOKENS_H",
            self.name.to_uppercase().replace('-', "_")
        );
        format!("#ifndef {guard}\n#define {guard}\n{parse_h}#endif /* {guard} */\n")
    }
}

