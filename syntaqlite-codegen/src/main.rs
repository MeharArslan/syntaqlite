use clap::{Parser, Subcommand};
use std::fs;
use std::path::{Path, PathBuf};
use std::process;

#[derive(Parser)]
#[command(name = "syntaqlite-codegen")]
#[command(about = "SQLite grammar extraction and code generation")]
struct Args {
    #[command(subcommand)]
    command: Command,
    #[arg(short, long, global = true)]
    verbose: bool,
}

#[derive(Subcommand)]
enum Command {
    Codegen {
        #[arg(long, required = true)]
        actions_dir: String,
        #[arg(long, required = true)]
        nodes_dir: String,
        #[arg(long, required = true)]
        tokenize_c: String,
        #[arg(long, default_value = "syntaqlite/csrc")]
        output_dir: String,
        /// Output directory for runtime engine files (tokenizer, keywords).
        /// If not set, these files are written to output_dir.
        #[arg(long)]
        runtime_csrc: Option<String>,
    },
    Lemon {
        #[arg(trailing_var_arg = true, allow_hyphen_values = true)]
        args: Vec<String>,
    },
    Mkkeyword {
        #[arg(trailing_var_arg = true, allow_hyphen_values = true)]
        args: Vec<String>,
    },
    /// Produce C amalgamation files (single-file compilation units).
    Amalgamate {
        /// Dialect name (e.g. "sqlite").
        #[arg(long, required = true)]
        dialect: String,
        /// Path to the syntaqlite-runtime crate root.
        #[arg(long, required = true)]
        runtime_dir: String,
        /// Path to the dialect crate root (e.g. syntaqlite/).
        #[arg(long, required = true)]
        dialect_dir: String,
        /// Output directory for generated files.
        #[arg(long, required = true)]
        output_dir: String,
        /// Emit only the runtime amalgamation.
        #[arg(long)]
        runtime_only: bool,
        /// Emit only the dialect amalgamation (references runtime header).
        #[arg(long)]
        dialect_only: bool,
    },
}

/// Delete any .c/.h files in `dir` whose first 512 bytes contain the autogenerated marker.
fn clean_generated_files(dir: &Path, verbose: bool) {
    let Ok(entries) = fs::read_dir(dir) else {
        return;
    };
    for entry in entries.flatten() {
        let path = entry.path();
        let ext = path.extension().and_then(|e| e.to_str()).unwrap_or("");
        if ext != "c" && ext != "h" {
            continue;
        }
        // Read just the first 512 bytes to check for the marker
        let Ok(content) = fs::read_to_string(&path) else {
            continue;
        };
        let prefix = &content[..content.len().min(512)];
        if prefix.contains(syntaqlite_codegen::c_writer::AUTOGENERATED_MARKER) {
            if verbose {
                eprintln!("  Removing stale generated file: {}", path.display());
            }
            let _ = fs::remove_file(&path);
        }
    }
}

fn main() {
    let args = Args::parse();

    let result = (|| -> Result<(), String> {
        match args.command {
            Command::Codegen {
                actions_dir,
                nodes_dir,
                tokenize_c,
                output_dir,
                runtime_csrc,
            } => {
                let temp_dir = tempfile::TempDir::new()
                    .map_err(|e| format!("Failed to create temp directory: {}", e))?;
                let work_dir = temp_dir
                    .path()
                    .to_str()
                    .ok_or_else(|| "Invalid temp directory path".to_string())?
                    .to_string();

                // Step 0: Parse node definitions
                if args.verbose {
                    eprintln!("Parsing node definitions...");
                }
                let nodes_path = Path::new(&nodes_dir);
                let mut synq_files: Vec<_> = fs::read_dir(nodes_path)
                    .map_err(|e| format!("Failed to read {}: {}", nodes_dir, e))?
                    .filter_map(|e| {
                        let p = e.ok()?.path();
                        (p.extension()?.to_str()? == "synq").then_some(p)
                    })
                    .collect();
                synq_files.sort();
                let mut all_items = Vec::new();
                for path in &synq_files {
                    let name = path.file_name().and_then(|s| s.to_str()).unwrap_or("?");
                    let content =
                        fs::read_to_string(path).map_err(|e| format!("{}: {}", name, e))?;
                    let items = syntaqlite_codegen::node_parser::parse_node(&content)
                        .map_err(|e| format!("{}: {}", name, e))?;
                    if args.verbose {
                        eprintln!("  {} ({} items)", name, items.len());
                    }
                    all_items.extend(items);
                }

                // Step 1: Generate parser (writes parse.c, parse.h into temp dir)
                if args.verbose {
                    eprintln!("Generating parser...");
                }
                syntaqlite_codegen::generate_parser(&actions_dir, &work_dir)?;

                // Step 2: Extract tokenizer
                if args.verbose {
                    eprintln!("Extracting tokenizer...");
                }
                let (tokenize_content, extract_result) =
                    syntaqlite_codegen::extract_tokenizer(&tokenize_c, "sqlite")?;

                // Step 3: Generate keyword hash
                if args.verbose {
                    eprintln!("Generating keyword hash...");
                }
                let keyword_c =
                    syntaqlite_codegen::generate_keyword_hash(&extract_result, "sqlite", &[])?;

                // Step 4: Clean stale generated files, then write outputs
                let out = Path::new(&output_dir);
                let include_dir = Path::new(&output_dir)
                    .parent()
                    .unwrap_or(Path::new("."))
                    .join("include/syntaqlite_sqlite");

                for dir in [out, include_dir.as_path()] {
                    if dir.is_dir() {
                        clean_generated_files(dir, args.verbose);
                    }
                }

                fs::create_dir_all(out)
                    .map_err(|e| format!("Failed to create output directory: {}", e))?;
                fs::create_dir_all(&include_dir)
                    .map_err(|e| format!("Failed to create include directory: {}", e))?;

                if args.verbose {
                    eprintln!("Writing output files...");
                }
                {
                    let tokens_content = fs::read_to_string(temp_dir.path().join("parse.h"))
                        .map_err(|e| format!("Failed to read parse.h: {}", e))?;
                    let guarded = format!(
                        "/*\n\
                         ** The author disclaims copyright to this source code.  In place of\n\
                         ** a legal notice, here is a blessing:\n\
                         **\n\
                         **    May you do good and not evil.\n\
                         **    May you find forgiveness for yourself and forgive others.\n\
                         **    May you share freely, never taking more than you give.\n\
                         **\n\
                         ** @generated by syntaqlite-codegen — DO NOT EDIT\n\
                         */\n\
                         \n\
                         #ifndef SYNTAQLITE_SQLITE_TOKENS_H\n\
                         #define SYNTAQLITE_SQLITE_TOKENS_H\n\
                         \n\
                         {tokens_content}\
                         \n\
                         #endif  // SYNTAQLITE_SQLITE_TOKENS_H\n",
                    );
                    fs::write(include_dir.join("sqlite_tokens.h"), guarded)
                        .map_err(|e| format!("Failed to write sqlite_tokens.h: {}", e))?;
                }

                // Step 4b: Generate AST code from node definitions
                let ast_nodes_h =
                    syntaqlite_codegen::ast_codegen::generate_ast_nodes_h(&all_items, "sqlite");
                fs::write(include_dir.join("sqlite_node.h"), ast_nodes_h)
                    .map_err(|e| format!("Failed to write sqlite_node.h: {}", e))?;

                let ast_builder_h =
                    syntaqlite_codegen::ast_codegen::generate_ast_builder_h(&all_items, "sqlite");
                fs::write(out.join("dialect_builder.h"), ast_builder_h)
                    .map_err(|e| format!("Failed to write dialect_builder.h: {}", e))?;

                // Runtime engine files (tokenizer, keywords, parse engine) go to runtime_csrc if set.
                let rt_out = runtime_csrc.as_deref().map(Path::new).unwrap_or(out);
                fs::create_dir_all(rt_out)
                    .map_err(|e| format!("Failed to create runtime csrc directory: {}", e))?;
                if runtime_csrc.is_some() {
                    clean_generated_files(rt_out, args.verbose);
                }

                let raw_parse_c = fs::read_to_string(temp_dir.path().join("parse.c"))
                    .map_err(|e| format!("Failed to read parse.c: {}", e))?;
                fs::write(out.join("sqlite_parse.c"), raw_parse_c)
                    .map_err(|e| format!("Failed to write sqlite_parse.c: {}", e))?;

                fs::write(out.join("sqlite_tokenize.c"), tokenize_content)
                    .map_err(|e| format!("Failed to write sqlite_tokenize.c: {}", e))?;

                fs::write(out.join("sqlite_keyword.c"), keyword_c)
                    .map_err(|e| format!("Failed to write sqlite_keyword.c: {}", e))?;

                let keyword_h = syntaqlite_codegen::generate_keyword_h();
                fs::write(out.join("sqlite_keyword.h"), keyword_h)
                    .map_err(|e| format!("Failed to write sqlite_keyword.h: {}", e))?;

                // Step 5: Generate Rust bindings
                if args.verbose {
                    eprintln!("Generating Rust bindings...");
                }
                let rust_src_dir = Path::new(&output_dir)
                    .parent()
                    .unwrap_or(Path::new("."))
                    .join("src");

                let parse_h_content = fs::read_to_string(temp_dir.path().join("parse.h"))
                    .map_err(|e| format!("Failed to read parse.h: {}", e))?;
                let token_defines = syntaqlite_codegen::extract_token_defines(&parse_h_content);

                let rust_tokens =
                    syntaqlite_codegen::ast_codegen::generate_rust_tokens(&token_defines);
                fs::write(rust_src_dir.join("tokens.rs"), rust_tokens)
                    .map_err(|e| format!("Failed to write tokens.rs: {}", e))?;

                let rust_ffi = syntaqlite_codegen::ast_codegen::generate_rust_ffi_nodes(&all_items);
                fs::write(rust_src_dir.join("ffi.rs"), rust_ffi)
                    .map_err(|e| format!("Failed to write ffi.rs: {}", e))?;

                let rust_ast = syntaqlite_codegen::ast_codegen::generate_rust_ast(&all_items);
                fs::write(rust_src_dir.join("ast.rs"), rust_ast)
                    .map_err(|e| format!("Failed to write ast.rs: {}", e))?;

                let rust_lib =
                    syntaqlite_codegen::ast_codegen::generate_rust_lib("syntaqlite_sqlite_dialect");
                fs::write(rust_src_dir.join("lib.rs"), rust_lib)
                    .map_err(|e| format!("Failed to write lib.rs: {}", e))?;

                let rust_wrappers = syntaqlite_codegen::ast_codegen::generate_rust_wrappers();
                fs::write(rust_src_dir.join("wrappers.rs"), rust_wrappers)
                    .map_err(|e| format!("Failed to write wrappers.rs: {}", e))?;

                // Step 6: Generate C AST metadata and fmt data headers
                if args.verbose {
                    eprintln!("Generating C AST metadata...");
                }
                let ast_meta_h =
                    syntaqlite_codegen::ast_codegen::generate_c_field_meta(&all_items, "sqlite");
                fs::write(out.join("dialect_meta.h"), ast_meta_h)
                    .map_err(|e| format!("Failed to write dialect_meta.h: {}", e))?;

                let fmt_data_h = syntaqlite_codegen::ast_codegen::generate_c_fmt_arrays(&all_items);
                fs::write(out.join("dialect_fmt.h"), fmt_data_h)
                    .map_err(|e| format!("Failed to write dialect_fmt.h: {}", e))?;

                // Step 7: Generate dialect descriptor and public API header
                if args.verbose {
                    eprintln!("Generating dialect descriptor...");
                }
                let dialect_c = syntaqlite_codegen::ast_codegen::generate_dialect_c("sqlite");
                fs::write(out.join("dialect.c"), dialect_c)
                    .map_err(|e| format!("Failed to write dialect.c: {}", e))?;

                let dialect_h = syntaqlite_codegen::ast_codegen::generate_dialect_h("sqlite");
                fs::write(include_dir.join("sqlite.h"), dialect_h)
                    .map_err(|e| format!("Failed to write sqlite.h: {}", e))?;

                let dialect_dispatch_h =
                    syntaqlite_codegen::ast_codegen::generate_dialect_dispatch_h("sqlite");
                fs::write(out.join("sqlite_dialect_dispatch.h"), dialect_dispatch_h)
                    .map_err(|e| format!("Failed to write sqlite_dialect_dispatch.h: {}", e))?;

                if args.verbose {
                    eprintln!("Code generation complete");
                }

                Ok(())
            }
            Command::Lemon { args: lemon_args } => {
                if args.verbose {
                    eprintln!("Running lemon with args: {:?}", lemon_args);
                }

                syntaqlite_codegen::lemon::run_lemon(&lemon_args);
            }
            Command::Mkkeyword {
                args: mkkeyword_args,
            } => {
                if args.verbose {
                    eprintln!("Running mkkeyword with args: {:?}", mkkeyword_args);
                }

                syntaqlite_codegen::mkkeyword::run_mkkeyword(&mkkeyword_args);
            }
            Command::Amalgamate {
                dialect,
                runtime_dir,
                dialect_dir,
                output_dir,
                runtime_only,
                dialect_only,
            } => {
                use syntaqlite_codegen::amalgamate;

                let out = Path::new(&output_dir);
                fs::create_dir_all(out)
                    .map_err(|e| format!("Failed to create output directory: {e}"))?;

                let runtime = PathBuf::from(&runtime_dir);
                let dialect_path = PathBuf::from(&dialect_dir);

                if runtime_only {
                    if args.verbose {
                        eprintln!("Generating runtime amalgamation...");
                    }
                    let result = amalgamate::amalgamate_runtime(&runtime)?;
                    fs::write(out.join("syntaqlite_runtime.h"), &result.header)
                        .map_err(|e| format!("writing header: {e}"))?;
                    fs::write(out.join("syntaqlite_runtime.c"), &result.source)
                        .map_err(|e| format!("writing source: {e}"))?;
                    if let Some(ext) = &result.ext_header {
                        fs::write(out.join("syntaqlite_ext.h"), ext)
                            .map_err(|e| format!("writing ext header: {e}"))?;
                    }
                    if args.verbose {
                        eprintln!("Wrote syntaqlite_runtime.{{h,c}} + syntaqlite_ext.h");
                    }
                } else if dialect_only {
                    if args.verbose {
                        eprintln!("Generating dialect amalgamation...");
                    }
                    let result = amalgamate::amalgamate_dialect(&dialect, &dialect_path)?;
                    fs::write(out.join(format!("syntaqlite_{dialect}.h")), &result.header)
                        .map_err(|e| format!("writing header: {e}"))?;
                    fs::write(out.join(format!("syntaqlite_{dialect}.c")), &result.source)
                        .map_err(|e| format!("writing source: {e}"))?;
                    if args.verbose {
                        eprintln!("Wrote syntaqlite_{dialect}.{{h,c}}");
                    }
                } else {
                    if args.verbose {
                        eprintln!("Generating full amalgamation...");
                    }
                    let result = amalgamate::amalgamate_full(&dialect, &runtime, &dialect_path)?;
                    fs::write(out.join(format!("syntaqlite_{dialect}.h")), &result.header)
                        .map_err(|e| format!("writing header: {e}"))?;
                    fs::write(out.join(format!("syntaqlite_{dialect}.c")), &result.source)
                        .map_err(|e| format!("writing source: {e}"))?;
                    if args.verbose {
                        eprintln!("Wrote syntaqlite_{dialect}.{{h,c}}");
                    }
                }

                Ok(())
            }
        }
    })();

    if let Err(e) = result {
        eprintln!("Error: {}", e);
        process::exit(1);
    }
}
