#[derive(Debug, Clone)]
pub struct DialectNaming {
    name: String,
}

impl DialectNaming {
    pub fn new(name: impl Into<String>) -> Self {
        Self { name: name.into() }
    }

    pub fn name(&self) -> &str {
        &self.name
    }

    pub fn include_dir_name(&self) -> String {
        format!("syntaqlite_{}", self.name)
    }

    pub fn tokens_header_guard(&self) -> String {
        format!("SYNTAQLITE_{}_TOKENS_H", self.name.to_uppercase())
    }

    pub fn tokens_header_name(&self) -> String {
        format!("{}_tokens.h", self.name)
    }

    pub fn node_header_name(&self) -> String {
        format!("{}_node.h", self.name)
    }

    pub fn dialect_header_name(&self) -> String {
        format!("{}.h", self.name)
    }

    pub fn dialect_dispatch_header_name(&self) -> String {
        format!("{}_dialect_dispatch.h", self.name)
    }

    pub fn dialect_symbol_fn_name(&self) -> String {
        format!("syntaqlite_{}_dialect", self.name)
    }

    pub fn parser_symbol_prefix(&self) -> String {
        format!("Synq{}Parse", util::pascal_case(&self.name))
    }

    pub fn guarded_tokens_header(&self, tokens_content: &str) -> String {
        let guard = self.tokens_header_guard();
        format!(
            "/*\n\
             ** The author disclaims copyright to this source code.  In place of\n\
             ** a legal notice, here is a blessing:\n\
             **\n\
             **    May you do good and not evil.\n\
             **    May you find forgiveness for yourself and forgive others.\n\
             **    May you share freely, never taking more than you give.\n\
             **\n\
             ** @generated by syntaqlite-codegen — DO NOT EDIT\n\
             */\n\
             \n\
             #ifndef {guard}\n\
             #define {guard}\n\
             \n\
             {tokens_content}\
             \n\
             #endif  // {guard}\n",
        )
    }
}

