use std::env;
use std::fs;
use std::path::PathBuf;

fn main() {
    // Get the path to vendored SQLite tools in this crate
    let manifest_dir = env::var("CARGO_MANIFEST_DIR").unwrap();
    let sqlite_dir = PathBuf::from(&manifest_dir).join("sqlite");
    let tokenize_c =
        PathBuf::from(&manifest_dir).join("../third_party/src/sqlite/src/tokenize.c");

    let lemon_path = sqlite_dir.join("lemon.c");
    let mkkeywordhash_path = sqlite_dir.join("mkkeywordhash.c");

    println!("cargo:rerun-if-changed={}", lemon_path.display());
    println!("cargo:rerun-if-changed={}", mkkeywordhash_path.display());
    println!("cargo:rerun-if-changed={}", tokenize_c.display());
    println!(
        "cargo:rustc-env=SYNTAQLITE_SQLITE_TOKENIZE_C={}",
        tokenize_c.display()
    );

    // Compile lemon.c with main renamed to lemon_main
    cc::Build::new()
        .file(&lemon_path)
        .define("main", "lemon_main")
        .compile("lemon");

    // Transform mkkeywordhash.c to accept custom keywords
    let out_dir = env::var("OUT_DIR").unwrap();
    let modified_mkkeywordhash = PathBuf::from(&out_dir).join("mkkeywordhash_modified.c");

    transform_mkkeywordhash(&mkkeywordhash_path, &modified_mkkeywordhash)
        .expect("Failed to transform mkkeywordhash.c");

    // Compile the modified mkkeywordhash.c
    cc::Build::new()
        .file(&modified_mkkeywordhash)
        .flag_if_supported("-Wno-missing-field-initializers")
        .flag_if_supported("-Wno-unused-parameter")
        .flag_if_supported("-Wno-unused-variable")
        .flag_if_supported("-Wno-sign-compare")
        .compile("mkkeywordhash");

    println!("cargo:rerun-if-changed=build.rs");

    // Track grammar files for test rebuilds
    register_grammar_files();
    generate_base_file_tables().expect("failed to generate embedded base file tables");
}

fn register_grammar_files() {
    use std::path::PathBuf;

    let manifest_dir = env::var("CARGO_MANIFEST_DIR").unwrap();
    let base_dir = PathBuf::from(&manifest_dir);

    // Track the base SQLite grammar
    let sqlite_parse_y = base_dir.join("../third_party/src/sqlite/src/parse.y");
    if sqlite_parse_y.exists() {
        println!("cargo:rerun-if-changed={}", sqlite_parse_y.display());
    }

    // Track all parser action files
    let parser_actions_dir = base_dir.join("../syntaqlite/parser-actions");
    if parser_actions_dir.exists() {
        println!("cargo:rerun-if-changed={}", parser_actions_dir.display());

        if let Ok(entries) = fs::read_dir(&parser_actions_dir) {
            for entry in entries.flatten() {
                let path = entry.path();
                if path.extension().and_then(|s| s.to_str()) == Some("y") {
                    println!("cargo:rerun-if-changed={}", path.display());
                }
            }
        }
    }

    // Track parser nodes directory too
    let parser_nodes_dir = base_dir.join("../syntaqlite/parser-nodes");
    if parser_nodes_dir.exists() {
        println!("cargo:rerun-if-changed={}", parser_nodes_dir.display());
    }
}

fn generate_base_file_tables() -> Result<(), String> {
    let manifest_dir = PathBuf::from(
        env::var("CARGO_MANIFEST_DIR").map_err(|e| format!("missing CARGO_MANIFEST_DIR: {e}"))?,
    );
    let out_dir = PathBuf::from(env::var("OUT_DIR").map_err(|e| format!("missing OUT_DIR: {e}"))?);

    let y_dir = manifest_dir.join("../syntaqlite/parser-actions");
    let synq_dir = manifest_dir.join("../syntaqlite/parser-nodes");

    let y_files = list_files_with_ext(&y_dir, "y")?;
    let synq_files = list_files_with_ext(&synq_dir, "synq")?;

    let mut out = String::new();
    out.push_str("// @generated by syntaqlite-codegen/build.rs — DO NOT EDIT\n\n");
    out.push_str("pub const BASE_Y_FILES: &[(&str, &str)] = &[\n");
    for file in &y_files {
        out.push_str(&format!(
            "    (\"{file}\", include_str!(concat!(env!(\"CARGO_MANIFEST_DIR\"), \"/../syntaqlite/parser-actions/{file}\"))),\n"
        ));
    }
    out.push_str("];\n\n");

    out.push_str("pub const BASE_SYNQ_FILES: &[(&str, &str)] = &[\n");
    for file in &synq_files {
        out.push_str(&format!(
            "    (\"{file}\", include_str!(concat!(env!(\"CARGO_MANIFEST_DIR\"), \"/../syntaqlite/parser-nodes/{file}\"))),\n"
        ));
    }
    out.push_str("];\n");

    let dest = out_dir.join("base_files_tables.rs");
    fs::write(&dest, out).map_err(|e| format!("failed to write {}: {e}", dest.display()))?;
    println!("cargo:rerun-if-changed={}", y_dir.display());
    println!("cargo:rerun-if-changed={}", synq_dir.display());
    Ok(())
}

fn list_files_with_ext(dir: &PathBuf, ext: &str) -> Result<Vec<String>, String> {
    if !dir.is_dir() {
        return Err(format!("missing directory {}", dir.display()));
    }
    let mut files = Vec::new();
    let entries = fs::read_dir(dir).map_err(|e| format!("reading {}: {e}", dir.display()))?;
    for entry in entries {
        let entry = entry.map_err(|e| format!("reading entry in {}: {e}", dir.display()))?;
        let path = entry.path();
        if path.extension().and_then(|s| s.to_str()) != Some(ext) {
            continue;
        }
        let Some(name) = path.file_name().and_then(|s| s.to_str()) else {
            continue;
        };
        files.push(name.to_string());
    }
    files.sort();
    Ok(files)
}

fn transform_mkkeywordhash(input: &PathBuf, output: &PathBuf) -> Result<(), String> {
    let content = fs::read_to_string(input)
        .map_err(|e| format!("Failed to read {}: {}", input.display(), e))?;

    // Inline the CTransformer operations as direct string manipulations.
    // Order matters: transform function signatures first, then call sites,
    // then rename.
    let mut s = content;

    // remove_static("aKeywordTable") + add_const("Keyword aKeywordTable")
    s = s.replacen("static Keyword aKeywordTable", "const Keyword aKeywordTable", 1);

    // remove_static("nKeyword") + add_const("int nKeyword")
    s = s.replacen("static int nKeyword", "const int nKeyword", 1);

    // add_function_parameters: findById, reorder, main
    s = s.replacen(
        "findById(int id){",
        "findById(int id, Keyword *aKeywordTable, int nKeyword){",
        1,
    );
    s = s.replacen(
        "reorder(int *pFrom){",
        "reorder(int *pFrom, Keyword *aKeywordTable, int nKeyword){",
        1,
    );
    s = s.replacen(
        "main(int argc, char **argv){",
        "main(int argc, char **argv, Keyword *aKeywordTable, int nKeyword){",
        1,
    );

    // Fix call sites: append keyword args after existing args
    s = s.replace(
        "findById(p->id)",
        "findById(p->id, aKeywordTable, nKeyword)",
    );
    s = s.replace(
        "findById(p->substrId)",
        "findById(p->substrId, aKeywordTable, nKeyword)",
    );
    s = s.replace(
        "reorder(&aKWHash[h])",
        "reorder(&aKWHash[h], aKeywordTable, nKeyword)",
    );
    // Fix recursive call in reorder function itself
    s = s.replace(
        "reorder(&aKeywordTable[i].iNext)",
        "reorder(&aKeywordTable[i].iNext, aKeywordTable, nKeyword)",
    );

    // rename_function("main", "mkkeyword_main")
    s = s.replace("main(int argc", "mkkeyword_main(int argc");

    // add_system_include("stddef.h")
    s = format!("#include <stddef.h>\n{}", s);

    // append struct layout exports
    s.push_str(concat!(
        "const size_t keyword_sizeof = sizeof(struct Keyword);\n",
        "const size_t keyword_offsetof_zName = offsetof(struct Keyword, zName);\n",
        "const size_t keyword_offsetof_zTokenType = offsetof(struct Keyword, zTokenType);\n",
        "const size_t keyword_offsetof_mask = offsetof(struct Keyword, mask);\n",
        "const size_t keyword_offsetof_priority = offsetof(struct Keyword, priority);\n",
        "const size_t keyword_offsetof_id = offsetof(struct Keyword, id);\n",
        "const size_t keyword_offsetof_hash = offsetof(struct Keyword, hash);\n",
        "const size_t keyword_offsetof_offset = offsetof(struct Keyword, offset);\n",
        "const size_t keyword_offsetof_len = offsetof(struct Keyword, len);\n",
        "const size_t keyword_offsetof_prefix = offsetof(struct Keyword, prefix);\n",
        "const size_t keyword_offsetof_longestSuffix = offsetof(struct Keyword, longestSuffix);\n",
        "const size_t keyword_offsetof_iNext = offsetof(struct Keyword, iNext);\n",
        "const size_t keyword_offsetof_substrId = offsetof(struct Keyword, substrId);\n",
        "const size_t keyword_offsetof_substrOffset = offsetof(struct Keyword, substrOffset);\n",
        "const size_t keyword_offsetof_zOrigName = offsetof(struct Keyword, zOrigName);\n",
    ));

    fs::write(output, s)
        .map_err(|e| format!("Failed to write {}: {}", output.display(), e))?;

    Ok(())
}
