use std::fs;
use std::path::Path;
use std::path::PathBuf;

use clap::Subcommand;

/// SQLite-specific codegen CLI subcommands.
///
/// Flattened into the top-level `Command` enum so the CLI interface is unchanged.
#[derive(Subcommand)]
pub(crate) enum CodegenCommand {
    /// Run the full codegen pipeline (grammar extraction, parser generation,
    /// tokenizer extraction, keyword hash, AST metadata, Rust bindings).
    Codegen {
        #[arg(long, required = true)]
        actions_dir: String,
        #[arg(long, required = true)]
        nodes_dir: String,
        #[arg(long, default_value = "syntaqlite-parser-sys/csrc/sqlite")]
        output_dir: String,
        /// Directory for generated Rust outputs. When provided, Rust files are
        /// written relative to this path instead of being inferred from
        /// --output-dir. Use when C and Rust outputs live in different crates.
        #[arg(long)]
        rust_dir: Option<String>,
    },
    /// Produce C amalgamation files (single-file compilation units).
    Amalgamate {
        /// Dialect name (e.g. "sqlite").
        #[arg(long, required = true)]
        dialect: String,
        /// Path to the syntaqlite crate root (for engine C sources).
        #[arg(long, required = true)]
        runtime_dir: String,
        /// Path to the dialect crate root (e.g. syntaqlite/).
        #[arg(long, required = true)]
        dialect_dir: String,
        /// Output directory for generated files.
        #[arg(long, required = true)]
        output_dir: String,
        /// Emit only the runtime amalgamation.
        #[arg(long)]
        runtime_only: bool,
        /// Emit only the dialect amalgamation (references runtime header).
        #[arg(long)]
        dialect_only: bool,
    },
    // Hidden subcommands for codegen subprocess support.
    // generate_parser() and generate_keyword_hash() spawn current_exe() with
    // these subcommands. They must be present in any binary that calls codegen.
    #[command(hide = true)]
    Lemon {
        #[arg(trailing_var_arg = true, allow_hyphen_values = true)]
        args: Vec<String>,
    },
    #[command(hide = true)]
    Mkkeyword {
        #[arg(trailing_var_arg = true, allow_hyphen_values = true)]
        args: Vec<String>,
    },
}

/// Dispatch a SQLite codegen subcommand. Called from `run()` in lib.rs.
pub(crate) fn dispatch(command: CodegenCommand) -> Result<(), String> {
    match command {
        CodegenCommand::Codegen {
            actions_dir,
            nodes_dir,
            output_dir,
            rust_dir,
        } => handle_codegen(&actions_dir, &nodes_dir, &output_dir, rust_dir.as_deref()),
        CodegenCommand::Amalgamate {
            dialect,
            runtime_dir,
            dialect_dir,
            output_dir,
            runtime_only,
            dialect_only,
        } => handle_amalgamate(
            &dialect,
            &runtime_dir,
            &dialect_dir,
            &output_dir,
            runtime_only,
            dialect_only,
        ),
        CodegenCommand::Lemon { args } => syntaqlite_buildtools::run_lemon(&args),
        CodegenCommand::Mkkeyword { args } => syntaqlite_buildtools::run_mkkeyword(&args),
    }
}

use crate::fs_util::{ensure_dir, write_file};

/// Delete any .c/.h files in `dir` whose first 512 bytes contain the autogenerated marker.
fn clean_generated_files(dir: &Path) {
    let Ok(entries) = fs::read_dir(dir) else {
        return;
    };
    for entry in entries.flatten() {
        let path = entry.path();
        let ext = path.extension().and_then(|e| e.to_str()).unwrap_or("");
        if ext != "c" && ext != "h" {
            continue;
        }
        let Ok(content) = fs::read_to_string(&path) else {
            continue;
        };
        let prefix = &content[..content.len().min(512)];
        if prefix.contains(syntaqlite_buildtools::AUTOGENERATED_MARKER) {
            let _ = fs::remove_file(&path);
        }
    }
}

fn handle_codegen(
    actions_dir: &str,
    nodes_dir: &str,
    output_dir: &str,
    rust_dir: Option<&str>,
) -> Result<(), String> {
    let dialect = syntaqlite_buildtools::DialectNaming::new("sqlite");

    let y_files = syntaqlite_buildtools::read_named_files_from_dir(actions_dir, "y")?;
    let synq_files = syntaqlite_buildtools::read_named_files_from_dir(nodes_dir, "synq")?;

    let no_keywords: Vec<String> = Vec::new();
    let request = syntaqlite_buildtools::CodegenRequest {
        dialect: &dialect,
        y_files: &y_files,
        synq_files: &synq_files,
        extra_keywords: &no_keywords,
        parser_symbol_prefix: None,
        include_rust: true,
        crate_name: Some("syntaqlite"),
        base_synq_files: None,
        open_for_extension: true,
        dialect_c_includes: syntaqlite_buildtools::dialect_codegen::DialectCIncludes {
            internal: "csrc/sqlite/",
            public: "",
            dialect_include_dir: "syntaqlite",
        },
    };
    let artifacts = syntaqlite_buildtools::generate_codegen_artifacts(&request)?;
    let outputs = syntaqlite_buildtools::sqlite::output_manifest::sqlite_output_manifest(
        &dialect, artifacts,
    )?;

    // output_dir is csrc/sqlite/ — C crate root is two levels up.
    let out = Path::new(output_dir);
    let c_crate_root = out
        .parent()
        .and_then(|p| p.parent())
        .unwrap_or(Path::new("."));
    let include_dir = c_crate_root.join(format!("include/{}", dialect.include_dir_name()));

    // When --rust-dir is provided, Rust outputs go to a separate crate root.
    // Otherwise, they are inferred from the C output directory (backward compatible).
    let rust_crate_root = rust_dir.map_or_else(|| c_crate_root.to_path_buf(), PathBuf::from);

    // Clean stale generated files
    for dir in [out, include_dir.as_path()] {
        if dir.is_dir() {
            clean_generated_files(dir);
        }
    }

    // For the internal syntaqlite crate, generated Rust dialect modules
    // (tokens.rs, ffi.rs, ast.rs) live under src/sqlite/.
    let rust_sqlite_dir = rust_crate_root.join("src/sqlite");

    ensure_dir(out, "output directory")?;
    ensure_dir(&include_dir, "include directory")?;
    ensure_dir(&rust_sqlite_dir, "Rust sqlite src directory")?;

    use syntaqlite_buildtools::sqlite::output_manifest::OutputBucket;

    let rust_src_dir = rust_crate_root.join("src");

    for output in outputs {
        let dir: &Path = match output.bucket {
            OutputBucket::Include => &include_dir,
            OutputBucket::DialectCsrc => out,
            // Generated dialect modules go into src/sqlite/ for the internal crate.
            OutputBucket::RustDialectSrc | OutputBucket::RustSqliteSrc => &rust_sqlite_dir,
            // Shared crate-level Rust modules (e.g. ast_traits.rs) go into src/.
            OutputBucket::RustCrateSrc => &rust_src_dir,
            // Scaffolding files (lib.rs, wrappers.rs) and crate-root files
            // (build.rs, Cargo.toml) are hand-maintained for the internal crate.
            // These are only used when generating external dialect crates.
            OutputBucket::RustCrateScaffold | OutputBucket::CrateRoot => continue,
        };
        write_file(&dir.join(output.file_name), output.content)?;
    }

    Ok(())
}

fn write_amalgamation_outputs(
    out: &Path,
    header_name: &str,
    source_name: &str,
    result: &syntaqlite_buildtools::amalgamate::AmalgamateOutput,
) -> Result<(), String> {
    write_file(&out.join(header_name), &result.header)?;
    write_file(&out.join(source_name), &result.source)?;
    if let Some(ext) = &result.ext_header {
        write_file(&out.join("syntaqlite_dialect.h"), ext)?;
    }
    Ok(())
}

fn handle_amalgamate(
    dialect: &str,
    runtime_dir: &str,
    dialect_dir: &str,
    output_dir: &str,
    runtime_only: bool,
    dialect_only: bool,
) -> Result<(), String> {
    use syntaqlite_buildtools::amalgamate;

    let out = Path::new(output_dir);
    ensure_dir(out, "output directory")?;

    let runtime = PathBuf::from(runtime_dir);
    let dialect_path = PathBuf::from(dialect_dir);

    if runtime_only {
        let result = amalgamate::amalgamate_runtime(&runtime)?;
        write_amalgamation_outputs(out, "syntaqlite_runtime.h", "syntaqlite_runtime.c", &result)?;
    } else {
        let result = if dialect_only {
            amalgamate::amalgamate_dialect(dialect, &dialect_path, None, None)?
        } else {
            amalgamate::amalgamate_full(dialect, &runtime, &dialect_path)?
        };
        let base = format!("syntaqlite_{dialect}");
        write_amalgamation_outputs(out, &format!("{base}.h"), &format!("{base}.c"), &result)?;
    }

    Ok(())
}
